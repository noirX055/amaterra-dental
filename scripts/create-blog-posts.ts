async function createPosts() {
  const supabaseUrl = envVars.NEXT_PUBLIC_SUPABASE_URL;
  const supabaseKey = envVars.SUPABASE_SERVICE_ROLE_KEY; // Use service role key to bypass RLS

  if (!supabaseUrl || !supabaseKey) {
    console.error("Missing Supabase credentials in .env.local");
    console.error("URL:", supabaseUrl);
    console.error("Key:", supabaseKey ? "present" : "missing");
    process.exit(1);
  }

  const supabase = createClient(supabaseUrl, supabaseKey);

  for (const post of posts) {
    try {
      const { data, error } = await supabase
        .from("blog_posts")
        .insert(post)
        .select()
        .single();

      if (error) {
        console.error(`Error creating post "${post.title}":`, error);
      } else {
        console.log(`✓ Created: ${post.title}`);
      }
    } catch (err) {
      console.error(`Failed to create post "${post.title}":`, err);
    }
  }

  console.log("\nDone!");
}

createPosts();
